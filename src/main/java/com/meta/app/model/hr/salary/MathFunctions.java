package com.meta.app.model.hr.salary;

import java.math.BigDecimal;
import java.util.List;

public class MathFunctions {
    public static BigDecimal SUM(BigDecimal x,  BigDecimal y) {
        return x.add(y);
    }


    // Java 方法，用于计算 List<Number> 的平均值

    // Java 方法，模拟低代码中常见的 IF 函数
    public static Object IF(boolean condition, Object trueValue, Object falseValue) {
        return condition ? trueValue : falseValue;
    }
}
